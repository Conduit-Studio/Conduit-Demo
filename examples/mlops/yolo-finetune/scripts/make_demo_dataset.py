#!/usr/bin/env python3
"""Generate a tiny SYNTHETIC 2-class bbox dataset so the whole workflow runs end-to-end
without shipping real images.

  class 0 = circle    (drawn as a red ellipse)
  class 1 = rectangle (drawn as a blue rectangle)

Each image gets 1–3 shapes with exact YOLO labels (`class cx cy w h`, all normalised).
It's synthetic but a perfectly valid detection task — ultralytics will train + report a
real mAP on it. Swap in your own images + labels for a real run.

    uv run python scripts/make_demo_dataset.py            # 200 train / 40 val @ 640px
    uv run python scripts/make_demo_dataset.py --train 50 --val 10

Then upload data/dataset/ to S3 at the key referenced in config/sweep.json.
"""
from __future__ import annotations

import argparse
import random
from pathlib import Path

from PIL import Image, ImageDraw

ROOT = Path(__file__).resolve().parents[1] / "data" / "dataset"
CLASSES = ["circle", "rectangle"]


def make_split(split: str, n: int, imgsz: int, rng: random.Random) -> None:
    img_dir = ROOT / "images" / split
    lbl_dir = ROOT / "labels" / split
    img_dir.mkdir(parents=True, exist_ok=True)
    lbl_dir.mkdir(parents=True, exist_ok=True)

    for i in range(n):
        img = Image.new("RGB", (imgsz, imgsz), (238, 238, 238))
        draw = ImageDraw.Draw(img)
        lines: list[str] = []

        for _ in range(rng.randint(1, 3)):
            cls = rng.randint(0, 1)
            w = rng.randint(imgsz // 8, imgsz // 3)
            h = rng.randint(imgsz // 8, imgsz // 3)
            x = rng.randint(0, imgsz - w)
            y = rng.randint(0, imgsz - h)
            if cls == 0:
                draw.ellipse([x, y, x + w, y + h], fill=(214, 48, 49))
            else:
                draw.rectangle([x, y, x + w, y + h], fill=(45, 62, 196))
            cx = (x + w / 2) / imgsz
            cy = (y + h / 2) / imgsz
            lines.append(f"{cls} {cx:.6f} {cy:.6f} {w / imgsz:.6f} {h / imgsz:.6f}")

        img.save(img_dir / f"{split}_{i:04d}.jpg", quality=85)
        (lbl_dir / f"{split}_{i:04d}.txt").write_text("\n".join(lines) + "\n")


def write_data_yaml() -> None:
    (ROOT / "data.yaml").write_text(
        "# Generated by scripts/make_demo_dataset.py.\n"
        "# Paths are relative to THIS file = the `dataset` channel root inside the\n"
        "# training container (SageMaker mounts the channel at /opt/ml/input/data/dataset).\n"
        "path: .\n"
        "train: images/train\n"
        "val: images/val\n"
        f"nc: {len(CLASSES)}\n"
        f"names: {CLASSES}\n"
    )


def main() -> None:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--train", type=int, default=200)
    ap.add_argument("--val", type=int, default=40)
    ap.add_argument("--imgsz", type=int, default=640)
    ap.add_argument("--seed", type=int, default=7)
    args = ap.parse_args()

    rng = random.Random(args.seed)
    make_split("train", args.train, args.imgsz, rng)
    make_split("val", args.val, args.imgsz, rng)
    write_data_yaml()
    print(
        f"wrote {args.train} train + {args.val} val images + labels + data.yaml under {ROOT}\n"
        f"next: aws s3 sync {ROOT} s3://<your-bucket>/examples/mlops/yolo-finetune/data/dataset/"
    )


if __name__ == "__main__":
    main()
